package com.gadgetsinnepal.gadgetsinnepalapp;


public class sItem {
    public String img;
    public String mediumSizedImage;
    public String title;
    public String content;
    public String id;
    public String date;
    public String link;

    public String getMediumSizedImage(){ return mediumSizedImage;}

    public void setMediumSizedImage(String mediumSizedImage){ this.mediumSizedImage=mediumSizedImage; }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getDate() {
        return date;
    }

    public void setDate(){this.date=date;}

    public String getLink() {
        return link;
    }

    public void setLink(){this.link=link;}

}

